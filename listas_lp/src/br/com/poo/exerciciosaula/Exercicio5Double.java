package br.com.poo.exerciciosaula;

public class Exercicio5Double {

	public static void main(String[] args) {
		Double gastosJaneiro = 15000.0;
		Double gastosFevereiro = 23000.0;
		Double gastosMarco = 17000.0;
		
		System.out.println("Esse programa tem o objetivo de demonstrar os gastos trimestrais de nossa empresa");
		Double gastosTrimestreDouble = gastosJaneiro + gastosFevereiro + gastosMarco;
		System.out.println("\nOs gastos do trimestre na variavel Double foram de: " + gastosTrimestreDouble);
		

	}

}
